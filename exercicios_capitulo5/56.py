# life stage

life_stage = 5

if life_stage < 2:
    print(f'You are a babe')
elif life_stage < 4:
    print(f'You are a child')
elif life_stage < 13:
    print(f' You are a boy/girl.')
elif life_stage < 20:
    print(f'You are a teenager')
elif life_stage < 65:
    print(f'You are an adult.')
else:
    print(f'You are a old man')
