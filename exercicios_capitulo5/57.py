# five if instructions

list = ['banana', 'pineapple', 'orange', 'strawberry', 'melon']

if 'banana' in list:
    print(f'You really like {list[0].title()}!')
if 'pineapple' in list:
    print(f'You really like {list[1].title()}!')
if 'orange' in list:
    print(f'You really like {list[2].title()}!')
if 'strawberry' in list:
    print(f'You really like {list[3].title()}!')
if 'melon' in list:
    print(f'You really like {list[4].title()}!')

